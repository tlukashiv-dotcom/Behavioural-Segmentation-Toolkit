render_report <- function(
    output_format,
    output_file
) {

  write_log(
    "Rendering ",
    output_file,
    ".",
    file = log_file
  )

  tryCatch(
    {

      rmarkdown::render(
        input = rmd_file,
        output_format = output_format,
        output_file = output_file,
        output_dir = report_dir,
        knit_root_dir = report_dir,
        envir = new.env(
          parent = globalenv()
        ),
        quiet = TRUE
      )

      write_log(
        "Successfully rendered ",
        output_file,
        ".",
        file = log_file
      )

      TRUE
    },
    error = function(error) {

      write_log(
        "Failed to render ",
        output_file,
        ": ",
        conditionMessage(error),
        file = log_file
      )

      warning(
        "Could not render ",
        output_file,
        ": ",
        conditionMessage(error)
      )

      FALSE
    }
  )
}


