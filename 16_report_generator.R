  model_column <- intersect(
    c(
      "model_id",
      "candidate_id",
      "model_name",
      "model"
    ),
    names(validated_models)
  )

  score_column <- intersect(
    c(
      "validation_score",
      "overall_validation_score",
      "score"
    ),
    names(validated_models)
  )

  jaccard_column <- intersect(
    c(
      "mean_jaccard",
      "bootstrap_mean_jaccard",
      "jaccard_mean"
    ),
    names(validated_models)
  )

  if (length(score_column) > 0) {

    best_index <- which.max(
      suppressWarnings(
        as.numeric(
          validated_models[[
            score_column[1]
          ]]
        )
      )
    )

  } else {

    best_index <- 1
  }

  if (length(model_column) > 0) {
    final_model <- safe_text(
      validated_models[[
        model_column[1]
      ]][best_index]
    )
  }

  if (length(score_column) > 0) {
    validation_score <- safe_numeric(
      validated_models[[
        score_column[1]
      ]][best_index]
    )
  }

  if (length(jaccard_column) > 0) {
    mean_jaccard <- safe_numeric(
      validated_models[[
        jaccard_column[1]
      ]][best_index]
    )
  }
}


