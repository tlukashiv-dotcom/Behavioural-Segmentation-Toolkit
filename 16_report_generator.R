find_project_root <- function(start_dir = getwd()) {

  current <- normalizePath(
    start_dir,
    winslash = "/",
    mustWork = TRUE
  )

  for (i in seq_len(12)) {

    if (
      dir.exists(file.path(current, "data")) &&
      dir.exists(file.path(current, "outputs"))
    ) {
      return(current)
    }

    parent <- dirname(current)

    if (identical(parent, current)) {
      break
    }

    current <- parent
  }

  stop("Project root not found.")
}


create_directory <- function(path) {

  if (!dir.exists(path)) {
    dir.create(
      path,
      recursive = TRUE,
      showWarnings = FALSE
    )
  }

  invisible(path)
}


safe_read_csv <- function(path) {

  if (!file.exists(path)) {
    return(tibble::tibble())
  }

  readr::read_csv(
    path,
    show_col_types = FALSE
  )
}


first_existing_file <- function(paths) {

  existing <- paths[
    file.exists(paths)
  ]

  if (length(existing) == 0) {
    return(NA_character_)
  }

  existing[1]
}


read_first_existing_csv <- function(paths) {

  selected <- first_existing_file(paths)

  if (is.na(selected)) {
    return(tibble::tibble())
  }

  safe_read_csv(selected)
}


safe_text <- function(x, default = "Not available") {

  x <- as.character(x)

  x <- x[
    !is.na(x) &
      stringr::str_trim(x) != ""
  ]

  if (length(x) == 0) {
    return(default)
  }

  x[1]
}


safe_numeric <- function(x, default = NA_real_) {

  value <- suppressWarnings(
    as.numeric(x)
  )

  if (length(value) == 0 || is.na(value[1])) {
    return(default)
  }

  value[1]
}


format_percent <- function(x, digits = 1) {

  x <- suppressWarnings(
    as.numeric(x)
  )

  ifelse(
    is.na(x),
    "",
    paste0(
      formatC(
        x,
        format = "f",
        digits = digits
      ),
      "%"
    )
  )
}


escape_r_string <- function(x) {

  x |>
    stringr::str_replace_all("\\\\", "\\\\\\\\") |>
    stringr::str_replace_all('"', '\\\\"')
}


relative_path <- function(path, start) {

  path <- normalizePath(
    path,
    winslash = "/",
    mustWork = FALSE
  )

  start <- normalizePath(
    start,
    winslash = "/",
    mustWork = FALSE
  )

  prefix <- paste0(
    start,
    "/"
  )

  if (startsWith(path, prefix)) {
    return(
      substring(
        path,
        nchar(prefix) + 1
      )
    )
  }

  path
}


clean_names_for_display <- function(data) {

  if (nrow(data) == 0) {
    return(data)
  }

  names(data) <- names(data) |>
    stringr::str_replace_all("_", " ") |>
    stringr::str_to_title()

  data
}


write_log <- function(..., file) {

  message_text <- paste0(...)

  cat(
    format(
      Sys.time(),
      "%Y-%m-%d %H:%M:%S"
    ),
    " | ",
    message_text,
    "\n",
    file = file,
    append = TRUE,
    sep = ""
  )
}


