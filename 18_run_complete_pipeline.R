find_project_root <- function(start_dir = getwd()) {

  current <- normalizePath(
    start_dir,
    winslash = "/",
    mustWork = TRUE
  )

  for (i in seq_len(15)) {

    has_outputs <- dir.exists(
      file.path(
        current,
        "outputs"
      )
    )

    has_data <- dir.exists(
      file.path(
        current,
        "data"
      )
    )

    has_r_scripts <- length(
      list.files(
        current,
        pattern = "^[0-9]{2}_.*\\.R$",
        full.names = FALSE
      )
    ) > 0

    if (
      has_outputs &&
      (has_data || has_r_scripts)
    ) {
      return(current)
    }

    parent <- dirname(current)

    if (identical(parent, current)) {
      break
    }

    current <- parent
  }

  stop(
    "Project root could not be identified. ",
    "Run this script from inside the Behavioural Segmentation Toolkit project."
  )
}


create_directory <- function(path) {

  if (!dir.exists(path)) {
    dir.create(
      path,
      recursive = TRUE,
      showWarnings = FALSE
    )
  }

  invisible(path)
}


timestamp <- function() {

  format(
    Sys.time(),
    "%Y-%m-%d %H:%M:%S"
  )
}


format_duration <- function(seconds) {

  if (is.na(seconds)) {
    return(NA_character_)
  }

  if (seconds < 60) {
    return(
      paste0(
        round(seconds, 2),
        " sec"
      )
    )
  }

  if (seconds < 3600) {

    minutes <- floor(
      seconds / 60
    )

    remaining_seconds <- round(
      seconds - minutes * 60,
      1
    )

    return(
      paste0(
        minutes,
        " min ",
        remaining_seconds,
        " sec"
      )
    )
  }

  hours <- floor(
    seconds / 3600
  )

  minutes <- floor(
    (
      seconds -
        hours * 3600
    ) / 60
  )

  remaining_seconds <- round(
    seconds -
      hours * 3600 -
      minutes * 60,
    1
  )

  paste0(
    hours,
    " h ",
    minutes,
    " min ",
    remaining_seconds,
    " sec"
  )
}


write_log <- function(
    ...,
    log_file,
    echo = TRUE
) {

  text <- paste0(...)

  line <- paste0(
    timestamp(),
    " | ",
    text
  )

  cat(
    line,
    "\n",
    file = log_file,
    append = TRUE,
    sep = ""
  )

  if (echo) {
    cat(
      line,
      "\n",
      sep = ""
    )
  }

  invisible(line)
}


separator_line <- function(
    character = "=",
    width = 78
) {

  paste(
    rep(
      character,
      width
    ),
    collapse = ""
  )
}


extract_stage_number <- function(script_name) {

  value <- stringr::str_extract(
    basename(script_name),
    "^[0-9]{2}"
  )

  suppressWarnings(
    as.integer(value)
  )
}


safe_file_exists <- function(path) {

  !is.na(path) &&
    nzchar(path) &&
    file.exists(path)
}


find_script <- function(
    project_dir,
    candidates
) {

  # Values stored in a tibble list-column may arrive here as a nested list.
  # Convert them to a simple character vector before using file/path helpers.
  candidates <- unlist(
    candidates,
    recursive = TRUE,
    use.names = FALSE
  )

  candidates <- as.character(
    candidates
  )

  candidates <- candidates[
    !is.na(candidates) &
      nzchar(candidates)
  ]

  if (length(candidates) == 0) {
    return(NA_character_)
  }

  exact_paths <- file.path(
    project_dir,
    candidates
  )

  exact_match <- exact_paths[
    file.exists(exact_paths)
  ]

  if (length(exact_match) > 0) {
    return(
      normalizePath(
        exact_match[1],
        winslash = "/",
        mustWork = TRUE
      )
    )
  }

  candidate_stems <- tools::file_path_sans_ext(
    basename(candidates)
  )

  all_r_scripts <- list.files(
    project_dir,
    pattern = "\\.R$",
    full.names = TRUE,
    recursive = FALSE
  )

  all_stems <- tools::file_path_sans_ext(
    basename(all_r_scripts)
  )

  matched <- all_r_scripts[
    all_stems %in%
      candidate_stems
  ]

  if (length(matched) > 0) {
    return(
      normalizePath(
        matched[1],
        winslash = "/",
        mustWork = TRUE
      )
    )
  }

  NA_character_
}


check_stage_outputs <- function(
    project_dir,
    output_paths
) {

  # expected_outputs is also stored as a list-column.
  output_paths <- unlist(
    output_paths,
    recursive = TRUE,
    use.names = FALSE
  )

  output_paths <- as.character(
    output_paths
  )

  output_paths <- output_paths[
    !is.na(output_paths) &
      nzchar(output_paths)
  ]

  if (length(output_paths) == 0) {
    return(NA)
  }

  full_paths <- file.path(
    project_dir,
    output_paths
  )

  all(
    file.exists(
      full_paths
    )
  )
}


run_script_in_clean_environment <- function(
    script_path,
    project_dir,
    show_output = TRUE,
    warnings_are_errors = FALSE
) {

  script_environment <- new.env(
    parent = globalenv()
  )

  warnings <- character(0)

  output <- character(0)

  stage_error <- NULL

  execution_expression <- quote(
    sys.source(
      script_path,
      envir = script_environment,
      chdir = TRUE
    )
  )

  handler <- function() {

    withCallingHandlers(
      eval(
        execution_expression,
        envir = environment()
      ),
      warning = function(warning_condition) {

        warnings <<- c(
          warnings,
          conditionMessage(
            warning_condition
          )
        )

        if (warnings_are_errors) {

          stop(
            conditionMessage(
              warning_condition
            )
          )

        } else {

          invokeRestart(
            "muffleWarning"
          )
        }
      }
    )
  }

  tryCatch(
    {

      output <- capture.output(
        handler(),
        type = "output"
      )
    },
    error = function(error_condition) {

      stage_error <<-
        conditionMessage(
          error_condition
        )
    }
  )

  if (
    show_output &&
    length(output) > 0
  ) {

    cat(
      paste(
        output,
        collapse = "\n"
      ),
      "\n"
    )
  }

  list(
    success = is.null(stage_error),
    error = stage_error,
    warnings = unique(warnings),
    output = output
  )
}


