# Behavioural Segmentation Toolkit
#
# Purpose:
#   - Run the complete Behavioural Segmentation Toolkit from Stage 01 to Stage 17
#   - Validate that all required scripts exist
#   - Record execution time and status for every stage
#   - Stop safely when a required stage fails
#   - Produce a complete pipeline log and machine-readable execution summary
#
# Main usage:
#
#   source("18_run_complete_pipeline.R")
#
# Main outputs:
#
#   outputs/logs/18_complete_pipeline.log
#   outputs/logs/18_pipeline_execution_summary.csv
#   outputs/logs/18_pipeline_execution_summary.rds
#   outputs/logs/18_sessionInfo.txt
#
# Notes:
#   - By default, all stages 01–17 are run.
#   - Optional or unavailable scripts can be marked as non-required below.
#   - Set resume_from_stage to restart from a selected stage.
#   - Set continue_on_error = TRUE only for diagnostic use.
# =============================================================================


