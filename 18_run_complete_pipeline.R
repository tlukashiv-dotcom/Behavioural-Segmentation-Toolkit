# 9. Execute stages
# =============================================================================

for (
  row_index in
  seq_len(
    nrow(
      pipeline_registry
    )
  )
) {

  stage_row <- pipeline_registry[
    row_index,
    ,
    drop = FALSE
  ]

  stage_number <- stage_row$stage
  stage_name <- stage_row$stage_name
  script_path <- stage_row$script_path
  stage_required <- stage_row$required
  outputs_before <- stage_row$outputs_exist_before

  write_log(
    separator_line("-"),
    log_file = log_file
  )

  write_log(
    "STAGE ",
    sprintf(
      "%02d",
      stage_number
    ),
    " STARTED — ",
    stage_name,
    log_file = log_file
  )

  if (
    !stage_row$script_exists
  ) {

    write_log(
      "Stage skipped because the script is unavailable.",
      log_file = log_file
    )

    execution_results[[row_index]] <- tibble::tibble(
      stage = stage_number,
      stage_name = stage_name,
      script = NA_character_,
      required = stage_required,
      status = "SKIPPED_MISSING_SCRIPT",
      start_time = timestamp(),
      end_time = timestamp(),
      duration_seconds = 0,
      duration_formatted = "0 sec",
      outputs_existed_before = outputs_before,
      outputs_exist_after = outputs_before,
      warning_count = 0L,
      warnings = NA_character_,
      error = NA_character_
    )

    if (
      stage_required &&
      !continue_on_error
    ) {

      pipeline_failed <- TRUE
      break
    }

    next
  }

  if (
    !force_rerun &&
    isTRUE(
      outputs_before
    )
  ) {

    write_log(
      "Stage skipped because all expected outputs already exist.",
      log_file = log_file
    )

    execution_results[[row_index]] <- tibble::tibble(
      stage = stage_number,
      stage_name = stage_name,
      script = basename(
        script_path
      ),
      required = stage_required,
      status = "SKIPPED_OUTPUTS_EXIST",
      start_time = timestamp(),
      end_time = timestamp(),
      duration_seconds = 0,
      duration_formatted = "0 sec",
      outputs_existed_before = outputs_before,
      outputs_exist_after = outputs_before,
      warning_count = 0L,
      warnings = NA_character_,
      error = NA_character_
    )

    next
  }

  stage_start <- Sys.time()

  write_log(
    "Script: ",
    basename(
      script_path
    ),
    log_file = log_file
  )

  old_working_directory <- getwd()

  setwd(
    project_dir
  )

  stage_result <- run_script_in_clean_environment(
    script_path = script_path,
    project_dir = project_dir,
    show_output = show_stage_output,
    warnings_are_errors = warnings_are_errors
  )

  setwd(
    old_working_directory
  )

  stage_end <- Sys.time()

  stage_duration <- as.numeric(
    difftime(
      stage_end,
      stage_start,
      units = "secs"
    )
  )

  outputs_after <- check_stage_outputs(
    project_dir,
    stage_row$expected_outputs
  )

  if (
    stage_result$success
  ) {

    stage_status <- "SUCCESS"

    write_log(
      "STAGE ",
      sprintf(
        "%02d",
        stage_number
      ),
      " COMPLETED SUCCESSFULLY in ",
      format_duration(
        stage_duration
      ),
      log_file = log_file
    )

    if (
      length(
        stage_result$warnings
      ) > 0
    ) {

      write_log(
        "Warnings: ",
        paste(
          stage_result$warnings,
          collapse = " | "
        ),
        log_file = log_file
      )
    }

  } else {

    stage_status <- "FAILED"
    pipeline_failed <- TRUE

    write_log(
      "STAGE ",
      sprintf(
        "%02d",
        stage_number
      ),
      " FAILED after ",
      format_duration(
        stage_duration
      ),
      log_file = log_file
    )

    write_log(
      "Error: ",
      stage_result$error,
      log_file = log_file
    )
  }

  execution_results[[row_index]] <- tibble::tibble(
    stage = stage_number,
    stage_name = stage_name,
    script = basename(
      script_path
    ),
    required = stage_required,
    status = stage_status,
    start_time = format(
      stage_start,
      "%Y-%m-%d %H:%M:%S"
    ),
    end_time = format(
      stage_end,
      "%Y-%m-%d %H:%M:%S"
    ),
    duration_seconds = stage_duration,
    duration_formatted =
      format_duration(
        stage_duration
      ),
    outputs_existed_before = outputs_before,
    outputs_exist_after = outputs_after,
    warning_count = length(
      stage_result$warnings
    ),
    warnings = ifelse(
      length(
        stage_result$warnings
      ) == 0,
      NA_character_,
      paste(
        stage_result$warnings,
        collapse = " | "
      )
    ),
    error = ifelse(
      is.null(
        stage_result$error
      ),
      NA_character_,
      stage_result$error
    )
  )

  if (
    !stage_result$success &&
    stage_required &&
    !continue_on_error
  ) {

    write_log(
      "Pipeline stopped because a required stage failed.",
      log_file = log_file
    )

    break
  }
}


# =============================================================================
# 10. Consolidate execution results
# =============================================================================

execution_results <- execution_results[
  !vapply(
    execution_results,
    is.null,
    logical(1)
  )
]

if (
  length(
    execution_results
  ) > 0
) {

  execution_summary <- dplyr::bind_rows(
    execution_results
  )

} else {

  execution_summary <- tibble::tibble(
    stage = integer(),
    stage_name = character(),
    script = character(),
    required = logical(),
    status = character(),
    start_time = character(),
    end_time = character(),
    duration_seconds = numeric(),
    duration_formatted = character(),
    outputs_existed_before = logical(),
    outputs_exist_after = logical(),
    warning_count = integer(),
    warnings = character(),
    error = character()
  )
}

readr::write_csv(
  execution_summary,
  summary_csv,
  na = ""
)

saveRDS(
  execution_summary,
  summary_rds
)


# =============================================================================
# 11. Save reproducibility information
# =============================================================================

capture.output(
  sessionInfo(),
  file = session_file
)


# =============================================================================
# 12. Final pipeline status
# =============================================================================

pipeline_end <- Sys.time()

pipeline_duration <- as.numeric(
  difftime(
    pipeline_end,
    pipeline_start,
    units = "secs"
  )
)

successful_stages <- sum(
  execution_summary$status ==
    "SUCCESS",
  na.rm = TRUE
)

failed_stages <- sum(
  execution_summary$status ==
    "FAILED",
  na.rm = TRUE
)

skipped_stages <- sum(
  stringr::str_starts(
    execution_summary$status,
    "SKIPPED"
  ),
  na.rm = TRUE
)

pipeline_status <- if (
  failed_stages == 0 &&
  !pipeline_failed
) {
  "SUCCESS"
} else {
  "FAILED"
}


write_log(
  separator_line(),
  log_file = log_file
)

write_log(
  "BEHAVIOURAL SEGMENTATION TOOLKIT — COMPLETE PIPELINE FINISHED",
  log_file = log_file
)

write_log(
  separator_line(),
  log_file = log_file
)

write_log(
  "Pipeline status: ",
  pipeline_status,
  log_file = log_file
)

write_log(
  "Successful stages: ",
  successful_stages,
  log_file = log_file
)

write_log(
  "Failed stages: ",
  failed_stages,
  log_file = log_file
)

write_log(
  "Skipped stages: ",
  skipped_stages,
  log_file = log_file
)

write_log(
  "Total execution time: ",
  format_duration(
    pipeline_duration
  ),
  log_file = log_file
)

write_log(
  "Execution summary: ",
  summary_csv,
  log_file = log_file
)

write_log(
  "Pipeline log: ",
  log_file,
  log_file = log_file
)

write_log(
  separator_line(),
  log_file = log_file
)


# =============================================================================
# 13. Console summary
# =============================================================================

cat(
  "\n",
  separator_line(),
  "\n",
  "BEHAVIOURAL SEGMENTATION TOOLKIT — COMPLETE PIPELINE ",
  pipeline_status,
  "\n",
  separator_line(),
  "\n",
  "Stages selected:                ",
  min(
    pipeline_registry$stage
  ),
  "–",
  max(
    pipeline_registry$stage
  ),
  "\n",
  "Stages completed successfully:  ",
  successful_stages,
  "\n",
  "Stages failed:                  ",
  failed_stages,
  "\n",
  "Stages skipped:                 ",
  skipped_stages,
  "\n",
  "Total execution time:           ",
  format_duration(
    pipeline_duration
  ),
  "\n",
  "Execution summary:\n",
  summary_csv,
  "\n",
  "Pipeline log:\n",
  log_file,
  "\n",
  separator_line(),
  "\n",
  sep = ""
)


# =============================================================================
# 14. Raise final error when pipeline failed
# =============================================================================

if (
  pipeline_status != "SUCCESS"
) {

  failed_details <- execution_summary |>
    dplyr::filter(
      status == "FAILED"
    ) |>
    dplyr::transmute(
      detail = paste0(
        "Stage ",
        sprintf(
          "%02d",
          stage
        ),
        " — ",
        stage_name,
        ": ",
        error
      )
    ) |>
    dplyr::pull(
      detail
    )

  stop(
    "Complete pipeline did not finish successfully.\n",
    paste(
      failed_details,
      collapse = "\n"
    ),
    "\nSee the log file for details:\n",
    log_file,
    call. = FALSE
  )
}
